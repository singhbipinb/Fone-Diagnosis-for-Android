package com.kashisol.fonediagnosis;

class cpuinput {

    String mname;
    String mvalue;

    public cpuinput(String name, String value) {
        mname = name;
        mvalue = value;

    }

    public String getName() {
        return mname;
    }

    public String getMvalue() {
        return mvalue;
    }
}
